public class Calculator {
    
    private Calculator(){

    }
    public static double sum(double a, double b) {
        return a + b;
    }
    public static double multiply(double a, double b){
        return a * b;
    }
    public static double divide(double a, double b){
        return a / b;
    }
    public static double remainder(double a, double b){
        return a % b;
    }
    public static double sinfunc(double a){
        return Math.sin(a);
    }
    public static double cosfunc(double a){
        return Math.cos(a);
    }
    public static double tanfunc(double a){
        return Math.tan(a);
    }
}
    
    

